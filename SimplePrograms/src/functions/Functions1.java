package functions;

public class Functions1 {
    public static void main(String[] args) {
        System.out.println("Мама я корову подоил на лугу я сено накосил");
        System.out.println("Мама я дровишек нарубил борова в сарае накормил");
        System.out.println("А теперь под вечер я устал");
        System.out.println("Целый день я круто попахал");
        System.out.println("Трескать очень сильно я хочу");
        System.out.println("Что любую гадость проглочу");
        System.out.println();

        chorus();

        System.out.println("Знаешь мать с тобой мне хорошо");
        System.out.println("Но решил жениться я ещё");
        System.out.println("Ты мне мать невесту подыщи");
        System.out.println("Чтоб она варить умела щи");
        System.out.println("Я без щей и дня не проживу");
        System.out.println("В жёны даже дуру я возьму");
        System.out.println("Пусть она кривая и в прыщах");
        System.out.println("Лишь бы разбиралась она в щах");
        System.out.println();

        chorus();

        System.out.println("Мать иди сюда чего скажу в армию я мама ухожу");
        System.out.println("Мне повестка только что пришла");
        System.out.println("Ну вот опять ты плакать начала");
        System.out.println("Хорошо служить я буду мать");
        System.out.println("Буду все приказы выполнять");
        System.out.println("Ну а если не дадут мне щей");
        System.out.println("Убегу из армии ваще");
        System.out.println();

        chorus();
    }

    static void chorus() {
        System.out.println("Возьми мясо и хрящи навари крутые щи");
        System.out.println("Ты чугун прополощи и вари в чугуне щи");
        System.out.println("Кидай больше овощей не могу я жить без щей");
        System.out.println("Чтоб я толстый был ваще навари мне русских щей");
        System.out.println();
    }
}
